# 当谈论到 Python 的标准库时，以下是一些常用的标准库：
# （1）os: 提供与操作系统交互的功能，例如文件和目录操作。
# （2）sys: 提供对 Python 解释器的访问和控制。
# （3）math: 提供数学运算函数，例如三角函数、对数和幂运算。
# （4）random: 生成伪随机数的函数。
# （5）datetime: 处理日期和时间的函数和类。
# （6）re: 提供正则表达式(regex)功能，用于模式匹配和文本搜索
# （7）json: 用于解析和生成 JSON(JavaScript Object Notation) 格式的函数。
# （8）csv: 用于读取和写入 CSV(comma-Separated Values) 文件的函数。
# （9）urlib: 用于处理 URL(Uniform Resource Locator) 和进行网络请求的模块。’
